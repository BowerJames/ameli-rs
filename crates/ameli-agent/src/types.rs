#[cfg(test)]
mod tests {
    use super::*;
    use ameli_ai::types::TextContent;

    fn test_user_message(text: &str) -> AgentMessage {
        AgentMessage::User(ameli_ai::types::UserMessage::text(text))
    }

    fn test_message_entry(id: &str, parent_id: Option<&str>, text: &str) -> MessageEntry {
        MessageEntry {
            id: id.to_string(),
            parent_id: parent_id.map(String::from),
            timestamp: "2026-01-01T00:00:00Z".to_string(),
            message: test_user_message(text),
        }
    }

    #[test]
    fn entry_accessors_message() {
        let entry = SessionEntry::Message(test_message_entry("abc", None, "hi"));
        assert_eq!(entry.id(), "abc");
        assert!(entry.parent_id().is_none());
        assert_eq!(entry.timestamp(), "2026-01-01T00:00:00Z");
        assert_eq!(entry.entry_type(), "message");
    }

    #[test]
    fn entry_accessors_with_parent() {
        let entry = SessionEntry::ModelChange(ModelChangeEntry {
            id: "def".to_string(),
            parent_id: Some("abc".to_string()),
            timestamp: "2026-01-01T00:00:01Z".to_string(),
            provider: "openai".to_string(),
            model_id: "gpt-4o".to_string(),
        });
        assert_eq!(entry.id(), "def");
        assert_eq!(entry.parent_id(), Some("abc"));
        assert_eq!(entry.entry_type(), "model_change");
    }

    #[test]
    fn entry_display() {
        let entry = SessionEntry::ThinkingLevelChange(ThinkingLevelChangeEntry {
            id: "xyz".to_string(),
            parent_id: None,
            timestamp: "2026-01-01T00:00:00Z".to_string(),
            thinking_level: "high".to_string(),
        });
        assert_eq!(format!("{entry}"), "thinking_level_change(xyz)");
    }

    #[test]
    fn custom_message_content_text() {
        let content = CustomMessageContent::Text("hello".to_string());
        assert_eq!(content, CustomMessageContent::Text("hello".to_string()));
    }

    #[test]
    fn custom_message_content_rich() {
        let content =
            CustomMessageContent::Rich(vec![MediaContentBlock::Text(TextContent::new("rich"))]);
        assert!(matches!(content, CustomMessageContent::Rich(_)));
    }

    #[test]
    fn model_ref_equality() {
        let a = ModelRef {
            provider: "openai".to_string(),
            model_id: "gpt-4o".to_string(),
        };
        let b = ModelRef {
            provider: "openai".to_string(),
            model_id: "gpt-4o".to_string(),
        };
        assert_eq!(a, b);
    }

    #[test]
    fn all_entry_variants_have_distinct_type_names() {
        let entries: Vec<SessionEntry> = vec![
            SessionEntry::Message(test_message_entry("1", None, "a")),
            SessionEntry::ThinkingLevelChange(ThinkingLevelChangeEntry {
                id: "2".to_string(),
                parent_id: None,
                timestamp: String::new(),
                thinking_level: "off".to_string(),
            }),
            SessionEntry::ModelChange(ModelChangeEntry {
                id: "3".to_string(),
                parent_id: None,
                timestamp: String::new(),
                provider: "p".to_string(),
                model_id: "m".to_string(),
            }),
            SessionEntry::Compaction(CompactionEntry {
                id: "4".to_string(),
                parent_id: None,
                timestamp: String::new(),
                summary: "s".to_string(),
                first_kept_entry_id: "1".to_string(),
                tokens_before: 100,
                details: None,
                from_hook: false,
            }),
            SessionEntry::BranchSummary(BranchSummaryEntry {
                id: "5".to_string(),
                parent_id: None,
                timestamp: String::new(),
                from_id: "1".to_string(),
                summary: "s".to_string(),
                details: None,
                from_hook: false,
            }),
            SessionEntry::Custom(CustomEntry {
                id: "6".to_string(),
                parent_id: None,
                timestamp: String::new(),
                custom_type: "test".to_string(),
                data: None,
            }),
            SessionEntry::CustomMessage(CustomMessageEntry {
                id: "7".to_string(),
                parent_id: None,
                timestamp: String::new(),
                custom_type: "test".to_string(),
                content: CustomMessageContent::Text("hi".to_string()),
                display: true,
                details: None,
            }),
        ];

        let type_names: Vec<&str> = entries.iter().map(|e| e.entry_type()).collect();
        let unique: std::collections::HashSet<&str> = type_names.iter().copied().collect();
        assert_eq!(
            unique.len(),
            entries.len(),
            "all entry types should be distinct"
        );
    }
}
